    public void fetch() {
        try {
            ResultSet rs = tService.getData();

            int size = 0;
            if (rs.last()) {
                size = rs.getRow();
                rs.beforeFirst();
            }

            dataId = new Integer[size];

            int count = 0;
            while (rs.next()) {
                dataId[count] = rs.getInt("id");
                String no = String.valueOf(count + 1);
                String Nama = rs.getString("name");
                String treasury_total = rs.getString("treasury_total");

                String tbPerson[] = { no, Nama, treasury_total };
                DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();

                jScrollPane1.setBorder(null);
                jTable1.getTableHeader().setBackground(new java.awt.Color(108, 99, 255));
                jTable1.getTableHeader().setForeground(new java.awt.Color(255, 255, 255));
                DefaultTableCellRenderer CenterRenderer = new DefaultTableCellRenderer();
                CenterRenderer.setHorizontalAlignment(JLabel.CENTER);
                jTable1.getColumnModel().getColumn(0).setCellRenderer(CenterRenderer);

                tblModel.addRow(tbPerson);
                count++;
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

